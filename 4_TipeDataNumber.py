# Tipe Data Number
# Ada 4 type data number di python

# nilai int = bilangan bulat
bilanganPositif = 10
bilanganNegatif = -10
print(bilanganPositif)
print(bilanganNegatif)

# nilai float = bilangan desimal
bilanganDesimalPositif = 10.2
bilanganDesimalNegatif = -10.2
print(bilanganDesimalPositif)
print(bilanganDesimalNegatif)

# nilai complex = ubah bilangan real menjadi bilangan complex
bilanganComplex = 3.14j
print(bilanganComplex)

# nilai Long/Hexadesimal
bilanganHexadesimal = 0x10
print(bilanganHexadesimal)  # Output: 16
